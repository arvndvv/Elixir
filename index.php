              var email= $('#email').val();
      if(name=="" ||  company=="" || phone=="" || message =="" || email==""){
        Notiflix.Report.Warning("Warning","All fields are required","OK");
      }else{
                          $.ajax({
                        type: "POST",
                              url: "contact.php",
                            data: "&email="+email+"&message="+message+"&phone="+phone+"&company="+company+"&name="+name,
                                          success: function(html){

                                              if($.trim(html)=="eformat"){
                                                Notiflix.Report.Warning("Warning","Email is incorrect","OK");
                                              }else if($.trim(html)=="nnum"){
                                                Notiflix.Report.Warning("Warning","Phone number is incorrect","OK");
                                              }else if($.trim(html)=="nshort"){
                                                Notiflix.Report.Warning("Warning","Phone number is short","OK");
                                            }else if($.trim(html)=="nlong"){
                                                Notiflix.Report.Warning("Warning","Phone number is long","OK");
                                            }else{
                                              Notiflix.Report.Success("Done","Mail Sended","OK");
                                            }
                                              Notiflix.Loading.Remove();
             },
             beforeSend: function () {
                Notiflix.Loading.Init({ svgSize:"60px",messageFontSize:"13px",svgColor:"#420058",messageColor:"#560062",backgroundColor:"rgba(46,46,46,0.8)", });   
